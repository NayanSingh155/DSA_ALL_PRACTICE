// Description

// Given an array containing N space-separated integers, you have to assign each element some category number based on the following rules

// If the element is only divisible by 3 then its category number is 1.
// If the element is only divisible by 5 then its category number is 2.
// If the element is divisible by both 3 and 5 then its category number is 3.
// Print the array of numbers representing the category of the elements of the given array.


// Input
// Input Format

// The first line contains integer numbers N - the size of the array.

// The second-line contains N integer numbers a1, a2, ... aN — elements of the array.

// Constraints

// 1 <= N <= 10^4

// 1 <= a[i] <= 10^5


// Output
// Print the elements of the new array representing the category number of the element of the given array.


// Sample Input 1 

// 5
// 3 25 15 5 9
// Sample Output 1

// 1 2 3 2 1
// Sample Input 2 

// 4
// 6 20 12 5
// Sample Output 2

// 1 2 1 2
// Hint

// For the first sample, 3, 25, 15, 5, 9

// Elements divisible by 3 only are 3 and 9 which means category number 1.

// Elements divisible by 5 only are 5 and 25 which means category number 2.

